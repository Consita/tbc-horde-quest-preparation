local reverseLookup = {}
for profID,localePair in pairs(translations) do
    for _,name in pairs(localePair) do
        reverseLookup[name:lower()] = profID
    end
end

---@param profName string|nil
---@return number|nil
function CasualTBCPrep.Locale.Professions.GetProfessionID(profName)
    if not profName or profName == "" then return nil end
    return reverseLookup[profName:lower()]
end

---@param profID number|nil
---@return string|nil
function CasualTBCPrep.Locale.Professions.GetProfessionName(profID)
    if not profID or profID <= 0 then return nil end

    local outer = translations[profID]
    if outer == nil then return nil end

    return outer[GetLocale()] or outer["enUS"]
end