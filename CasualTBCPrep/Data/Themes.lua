---@param themeCode string
function CasualTBCPrep.Themes.ChangeTheme(themeCode)
    if themeCode == nil or themeCode == "" then return end
    local theme = themeData[themeCode]
    if theme == nil then return end

    CasualTBCPrep.Themes.SelectedTheme = theme
    CasualTBCPrep.W_Main.ReloadActiveTab()
end

function CasualTBCPrep.Themes.ChangeThemeDefault()
    CasualTBCPrep.Themes.ChangeTheme(CasualTBCPrep.Themes.DefaultThemeCode)
end